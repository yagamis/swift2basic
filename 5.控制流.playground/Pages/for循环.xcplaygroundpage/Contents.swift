//: for循环
//: 用于处理有固定次数的操作.
//:  - ⚡️for-in 对一个序列(集合类型等)的每一个项目进行一系列操作.
//:  - 传统for:执行一系列操作直到满足特定条件,通常用于每次循环结束后计数器的递增.

//:  - for-in:
//:  - 1⃣️序列中的每项都参与. for与in之间加上一个代替每项的名(常量), 比如打印从1到5.
for 数字 in 1...5 {
    print(数字)
}
//:  - 2⃣️只需要循环某操作固定次数,而无需每项参与,可以省略项名.比如3的10次方.
let 底 = 2
let 幂 = 10
var 结果 = 1

for 次数 in 1...幂 {
    结果 *= 底
}
结果

//:  - 传统for:   for 初始化;条件;增量 {操作语句}
//:  - 打印从1到5
for var 数字 = 1; 数字 <= 10; 数字 += 2 {
    print(数字)
}

